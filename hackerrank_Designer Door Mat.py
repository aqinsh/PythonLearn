
""" 
    Size: 7 x 21 
    ---------.|.---------
    ------.|..|..|.------
    ---.|..|..|..|..|.---
    -------WELCOME-------
    ---.|..|..|..|..|.---
    ------.|..|..|.------
    ---------.|.---------
    
    Size: 11 x 33
    ---------------.|.---------------
    ------------.|..|..|.------------
    ---------.|..|..|..|..|.---------
    ------.|..|..|..|..|..|..|.------
    ---.|..|..|..|..|..|..|..|..|.---
    -------------WELCOME-------------
    ---.|..|..|..|..|..|..|..|..|.---
    ------.|..|..|..|..|..|..|.------
    ---------.|..|..|..|..|.---------
    ------------.|..|..|.------------
    ---------------.|.---------------
"""
size = input().split()
n = int(size[0])
h = (n - 1) // 2
m = int(size[1])
c = (m - 3) // 2
cen = (m - 7) // 2

for i in range(h):
    print((c - 3 * i) * '-' + ('.|.') * i +
          '.|.' + ('.|.') * i + (c - 3 * i) * '-')
print(cen * '-' + 'WELCOME' + cen * '-')
for i in range((h + 1), n):
    t = n - i - 1
    print((c - 3 * t) * '-' + ('.|.') * t +
          '.|.' + ('.|.') * t + (c - 3 * t) * '-')
